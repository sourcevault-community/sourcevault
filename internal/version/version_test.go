package version

import (
	"runtime"
	"testing"
)

// TestCurrentVersion verifies that the application version metadata is correctly
// initialized with expected default values.
func TestCurrentVersion(t *testing.T) {
	if Current.AppName != "sourcevault" {
		t.Errorf("expected AppName sourcevault, got %s", Current.AppName)
	}

	if Current.AppVersion != "0.2.0-dev" {
		t.Errorf("expected AppVersion 0.2.0-dev, got %s", Current.AppVersion)
	}

	if Current.Architecture != runtime.GOARCH {
		t.Errorf("expected Architecture %s, got %s", runtime.GOARCH, Current.Architecture)
	}

	// GitBranch, GitCommit, and BuildDate might be UNKNOWN if not set via ldflags,
	// which is the case during go test unless specified.
	if Current.GitBranch == "" {
		t.Error("GitBranch should not be empty")
	}
}
